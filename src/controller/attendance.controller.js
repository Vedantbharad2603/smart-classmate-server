const attendance_service = require("../services/attendance.services");
exports.create = (req, res, next) => {
    const date = req.body.date; // Assuming date is sent in the request body
    attendance_service
    .create(date)
    .then((response) =>
        res.status(200).send({
            message: typeof response === "string" ? "Error" : "Success",
            data: response,
        })
    )
    .catch(next);
};

